# define the characteristics of a dog using a dictionary

bosco = {}

bosco["Name"] = "Bosco"
bosco["Age"] = 6
bosco["Breed"] = "Havanese"

faith = {}

faith["Name"] = "Faith"
faith["Age"] = 14
faith["Breed"] = "Havanese"

print()

# display the characteristics of the dog named Bosco

print(bosco)

print(f"The dog's name is: {bosco['Name']}")
print(f"The dog's age is: {bosco['Age']}")
print(f"The dog's breed is: {bosco['Breed']}")

print()

